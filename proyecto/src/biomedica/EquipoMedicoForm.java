    private void CargarDatos()
    {
        try {
            Conexion conn = new Conexion();
            Connection cn = conn.CreateConnection();
            PreparedStatement ps;
            String query = "select em.id, em.nombre, m.nombre as marca, em.costo, em.estado, em.caracteristicas from equipo_medico as em join marcas as m on m.id = em.marca_id";
            DefaultTableModel model = new DefaultTableModel(new String[] { "Id", "Nombre", "Marca", "Costo", "Estado", "Caracteristicas" }, 0);
            ps = cn.prepareStatement(query);
            ResultSet rs = ps.executeQuery();
            while(rs.next()) {
                int id = rs.getInt(1);
                String nombre = rs.getString(2);
                String marca = rs.getString(3);
                Double costo = rs.getDouble(4);
                String estado = rs.getString(5);
                String caracteristica = rs.getString(6);
                model.addRow(new Object[] {id, nombre, marca, costo, estado, caracteristica});
            }
            jTable.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(EquipoMedicoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    private void Buscar()
    {
        try {
            Conexion conn = new Conexion();
            Connection cn = conn.CreateConnection();
            PreparedStatement ps;
            String query = "select em.id, em.nombre, m.nombre as marca, em.costo, em.estado, em.caracteristicas from equipo_medico as em join marcas as m on m.id = em.marca_id where em.nombre = ?";
            DefaultTableModel model = new DefaultTableModel(new String[] { "Id", "Nombre", "Marca", "Costo", "Estado", "Caracteristicas" }, 0);
            ps = cn.prepareStatement(query);
            ps.setString(1, txtNombre.getText());
            ResultSet rs = ps.executeQuery();
            while(rs.next()) {
                int id = rs.getInt(1);
                String nombre = rs.getString(2);
                String marca = rs.getString(3);
                Double costo = rs.getDouble(4);
                String estado = rs.getString(5);
                String caracteristica = rs.getString(6);
                model.addRow(new Object[] {id, nombre, marca, costo, estado, caracteristica});
            }
            jTable.setModel(model);
        } catch (SQLException ex) {
            Logger.getLogger(EquipoMedicoForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
